
function show_hide(tab1, tab2)
{
    $("#" + tab1).show();
    $("#" + tab2).hide();

    $("." + tab1).addClass('active');
    $("." + tab2).removeClass('active');


}


function show_hide_reional(tab1, tab2, tab3)
{
//alert(tab2)	;
    $("#" + tab2).hide();
    $("#" + tab3).hide();
    $("#" + tab1).show();


    $("." + tab1).addClass('active');
    $("." + tab2).removeClass('active');
    $("." + tab3).removeClass('active');


}
